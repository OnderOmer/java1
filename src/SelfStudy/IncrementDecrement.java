package SelfStudy;

public class IncrementDecrement {

    public static void main(String[] args) {

        int apples =5;

        System.out.println(++apples);// +1 = 6
        System.out.println(--apples);//-1 = 4
        System.out.println(apples--);// 5
        System.out.println(apples++);//5



    }
}
